// 1.variable whose value holds a string.
let myString = "Hello, World!";
console.log("myString:", myString);

// 2.variable whose value holds a number.
let myNumber = 42;
console.log("myNumber:", myNumber);

// 3.variable whose value holds an object.
let myObject = { key: "value" };
console.log("myObject:", myObject);

// 4.variable whose value holds a boolean.
let myBoolean = true;
console.log("myBoolean:", myBoolean);

// 5.variable whose value holds null.
let myNull = null;
console.log("myNull:", myNull);

// 6.variable whose value is undefined.
let myUndefined;
console.log("myUndefined:", myUndefined);

// 7.variable whose value holds a symbol.
let mySymbol = Symbol("mySymbol");
console.log("mySymbol:", mySymbol);
